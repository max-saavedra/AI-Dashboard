def _generate_dataset_summary(df: pd.DataFrame, columns: list[ColumnProfile]) -> str:
    """
    Generate a heuristic human-readable summary of the dataset.
    This is a fallback when AI enrichment is unavailable.
    """
    row_count = len(df)
    col_count = len(df.columns)

    # Count column types
    numeric_cols = [c for c in columns if c.inferred_type == "numeric"]
    date_cols = [c for c in columns if c.inferred_type == "date"]
    categorical_cols = [c for c in columns if c.inferred_type == "categorical"]
    text_cols = [c for c in columns if c.inferred_type == "text"]

    # Build summary string
    parts = [
        f"Dataset contains {row_count} records with {col_count} attributes.",
    ]

    if numeric_cols:
        numeric_names = ", ".join([c.name for c in numeric_cols[:3]])
        count_more = len(numeric_cols) - 3
        more_text = f" and {count_more} more" if count_more > 0 else ""
        parts.append(f"Numeric measures: {numeric_names}{more_text}.")

    if date_cols:
        date_names = ", ".join([c.name for c in date_cols[:3]])
        parts.append(f"Time dimensions: {date_names}.")

    if categorical_cols:
        cat_names = ", ".join([c.name for c in categorical_cols[:3]])
        count_more = len(categorical_cols) - 3
        more_text = f" and {count_more} more" if count_more > 0 else ""
        parts.append(f"Categorical attributes: {cat_names}{more_text}.")

    return " ".join(parts)


def _profile_column(series: pd.Series) -> ColumnProfile:
    """Infer type and compute statistics for a single column."""
    name = str(series.name)
    null_rate = series.isna().mean()
    non_null = series.dropna()
    unique_count = int(non_null.nunique())

    inferred_type = _infer_type(non_null, unique_count, len(series))
    stats = _compute_stats(non_null, inferred_type)
    suggested_role = _suggest_role(inferred_type, unique_count, len(series))
    sample_values = _safe_sample(non_null)

    return ColumnProfile(
        name=name,
        inferred_type=inferred_type,
        null_rate=float(null_rate),
        unique_count=unique_count,
        stats=stats,
        suggested_role=suggested_role,
        sample_values=sample_values,
    )


def _infer_type(
    series: pd.Series, unique_count: int, total_rows: int
) -> str:
    """
    Attempt to cast the series to numeric or date; fall back to
    categorical or text based on cardinality.
    """
    if series.empty:
        return "text"

    # Try numeric
    numeric = pd.to_numeric(series, errors="coerce")
    if numeric.notna().mean() >= TYPE_CONFIDENCE_THRESHOLD:
        return "numeric"

    # Try date (infer_datetime_format removed in pandas 3.0).
    # Suppress the dateutil fallback warning – we intentionally let pandas
    # try common formats before falling through to categorical/text.
    try:
        import warnings as _warnings
        with _warnings.catch_warnings():
            _warnings.simplefilter("ignore", UserWarning)
            parsed = pd.to_datetime(series, errors="coerce")
        if parsed.notna().mean() >= TYPE_CONFIDENCE_THRESHOLD:
            return "date"
    except Exception:
        pass

    # Categorical vs free text
    cardinality_ratio = unique_count / max(total_rows, 1)
    if cardinality_ratio <= CATEGORICAL_CARDINALITY_RATIO:
        return "categorical"

    return "text"


def _compute_stats(series: pd.Series, inferred_type: str) -> dict[str, Any]:
    """Compute type-appropriate descriptive statistics."""
    if inferred_type == "numeric":
        numeric = pd.to_numeric(series, errors="coerce").dropna()
        if numeric.empty:
            return {}
        return {
            "min": round(float(numeric.min()), 4),
            "max": round(float(numeric.max()), 4),
            "mean": round(float(numeric.mean()), 4),
            "median": round(float(numeric.median()), 4),
            "std": round(float(numeric.std()), 4),
            "sum": round(float(numeric.sum()), 4),
        }

    if inferred_type == "date":
        dates = pd.to_datetime(series, errors="coerce").dropna()
        if dates.empty:
            return {}
        return {
            "min": str(dates.min().date()),
            "max": str(dates.max().date()),
        }

    # categorical / text
    top_values = series.value_counts().head(5).to_dict()
    return {"top_values": {str(k): int(v) for k, v in top_values.items()}}


def _suggest_role(
    inferred_type: str, unique_count: int, total_rows: int
) -> str:
    """
    Heuristically assign a BI role so the dashboard knows how to use
    the column (dimension, measure, datetime, or label).
    """
    if inferred_type == "date":
        return "datetime"
    if inferred_type == "numeric":
        return "measure"
    if inferred_type == "categorical":
        return "dimension"
    # text with very low cardinality (e.g., a "name" column) → label
    if unique_count / max(total_rows, 1) > 0.50:
        return "label"
    return "dimension"


def _safe_sample(series: pd.Series, n: int = 5) -> list[Any]:
    """Return up to n non-null sample values as plain Python types."""
    sample = series.head(n).tolist()
    return [
        (
            str(v) if not isinstance(v, (int, float, bool, type(None))) else v
        )
        for v in sample
    ]
