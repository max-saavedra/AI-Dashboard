"""
app/services/etl/profiler.py

Heuristic-based column profiling performed locally before AI schema detection.

Responsibilities:
  - Infer column data types (numeric, date, categorical, text)
  - Compute descriptive statistics per column
  - Identify candidate dimension and measure columns
  - Build the sample payload sent to the AI Schema Agent (RF-02)

Running heuristics first reduces token usage and provides a fallback
when the AI provider is unavailable (RNF-04).
"""

from __future__ import annotations

from dataclasses import dataclass, field
from typing import Any, Optional

import numpy as np
import pandas as pd

from app.core.logging import get_logger

logger = get_logger(__name__)

# Columns with fewer unique values than this ratio are treated as categorical
CATEGORICAL_CARDINALITY_RATIO = 0.10
# Minimum share of parseable values to declare a column numeric or date
TYPE_CONFIDENCE_THRESHOLD = 0.80


@dataclass
class ColumnProfile:
    """Metadata inferred for a single column."""

    name: str
    inferred_type: str          # "numeric" | "date" | "categorical" | "text"
    alias: str = ""             # human-readable name (filled by AI agent)
    sample_values: list[Any] = field(default_factory=list)
    null_rate: float = 0.0
    unique_count: int = 0
    stats: dict[str, Any] = field(default_factory=dict)
    suggested_role: str = ""    # "dimension" | "measure" | "datetime" | "label"


@dataclass
class DatasetProfile:
    """Aggregated profile of the entire dataset."""

    row_count: int
    column_count: int
    columns: list[ColumnProfile]
    # Human-readable description of the dataset (generated by AI or heuristic)
    dataset_summary: str = ""

    def to_ai_payload(self, sample_rows: int = 2, max_columns: int = 30) -> dict[str, Any]:
        """
        Build a minimal, token-efficient payload for the AI Schema Agent.
        OPTIMIZED: Limits to top columns and minimal samples to reduce token cost.

        Args:
            sample_rows: Number of sample values per column (default 2, was 5)
            max_columns: Maximum columns to include (default 30, rest ignored)
        """
        # Limit columns if there are too many (prevents huge payloads)
        limited_columns = self.columns[:max_columns]

        return {
            "row_count": self.row_count,
            "column_count": self.column_count,
            # Track how many cols were sent
            "columns_sent": len(limited_columns),
            "columns": [
                {
                    "name": col.name,
                    "inferred_type": col.inferred_type,
                    "null_rate": round(col.null_rate, 3),
                    "unique_count": col.unique_count,
                    "sample_values": col.sample_values[:sample_rows],
                    "stats": col.stats,
                }
                for col in limited_columns
            ],
        }


def profile_dataframe(df: pd.DataFrame) -> DatasetProfile:
    """
    Run heuristic profiling on a cleaned DataFrame.
    Returns a DatasetProfile with per-column metadata.
    """
    columns = [_profile_column(df[col]) for col in df.columns]

    # Generate a basic heuristic summary
    dataset_summary = _generate_dataset_summary(df, columns)

    profile = DatasetProfile(
        row_count=len(df),
        column_count=len(df.columns),
        columns=columns,
        dataset_summary=dataset_summary,
    )
    logger.info(
        "dataframe_profiled",
        rows=profile.row_count,
        columns=profile.column_count,
        has_summary=bool(dataset_summary),
    )
    return profile


